package org.example.domain;

/**
 * Enumeration representing keys for different types of sorting methods.
 */
public enum SortType {
    /**
     * Sorting by number.
     */
    SORT_NUMBER,
    /**
     * Sorting by model.
     */
    SORT_MODEL,
    /**
     * Sorting by mileage.
     */
    SORT_MILEAGE,
    /**
     * Conditional sorting.
     */
    SORT_CONDITION
}
